package ch03.exam01;

public class SignOperatorExample {

	public static void main(String[] args) {
		
		int x = -100;
		int result1 = +x;
		int result2 = -x;
		System.out.println("result 1 : " + result1);
		System.out.println("result 2 : " + result2);
		
		byte b = 100;
		int result3 = -b;
		
		System.out.println("result 3 : " + result3);

	}

}
